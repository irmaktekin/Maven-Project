public class MarioGame {
	//when the up button is pressed
	public void up() {
		System.out.println("jump");
	}
	public void down() {
		System.out.println("go into a hole");
	}
	public void right() {
		System.out.println("go faster");
	}
	public void left() {
		System.out.println("stop");
	}

}
